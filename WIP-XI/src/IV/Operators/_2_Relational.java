public class _2_Relational {
    public static void main(String[] args) {
        int x = 1;
        
        System.out.println("== : " + (x == 2));
        
        System.out.println("!= : " + (x != 3));
        
        System.out.println("< : " + (x < 1));
        
        System.out.println("> : " + (x > 9));
        
        System.out.println("<= : " + (x <= 12));
        
        System.out.println("<= : " + (x <= 8));
    }
}
